package Kalkulacka;


public class Kalkulacka {

    /**
     * metoda pro scitani
     * @param cislo1 - prvni cislo
     * @param cislo2 - druhe cislo
     * @return vysledek secteni
     */
    public int scitani(int cislo1, int cislo2) {
        return cislo1 + cislo2;
    }

    public int nasobeni(int cislo1, int cislo2) {
        return cislo1 * cislo2;
    }

    public int odecitani(int cislo1, int cislo2) {
        return cislo1 - cislo2;
    }


}
